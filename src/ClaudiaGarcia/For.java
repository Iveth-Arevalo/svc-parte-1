
package ClaudiaGarcia;

public class For {
    public static void main(String[] args) {
        System.out.println("Vamos a hacer un for");
        
        for (int i = 0; i <= 10; i++) {
            System.out.println("Valor de i = " + i );
        }
        System.out.println("\n");
    }
    
}
